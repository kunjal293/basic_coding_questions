//printing reverse using arrays
#include<iostream>
using namespace std;
int main()
{
   int a[10];
   int n;
   for(int i=0;i<=n;i++)
   {
     cin>>a[i];
   }
   for(int i=0;i<=n;i++)
   {
       cout<<a[i];
   }
   for(int i= n-1;i>=0;i--)
   {

       cout<<a[i];
   }
   cout<<endl;
    return 0;
}
