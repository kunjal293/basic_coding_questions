// ExplicitTypeConversion.cpp
#include <iostream>
using namespace std;

int main() {

	cout << 66 << endl;
	cout << (char)66 << endl;

	char ch = 'A';
	cout << ch << endl;
	cout << (int)ch << endl;

	cout << (char)67 << endl;
	cout << (int)('A') << endl;

	cout << (int)(10.11) << endl;

	return 0;
}

