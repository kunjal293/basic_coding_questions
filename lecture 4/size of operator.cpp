// Sizeof.cpp
#include <iostream>
using namespace std;

int main() {

	int a = 10;
	float f = 10.11;
	char c = 'A';

	cout << "a: " << sizeof(a) << endl;
	cout << "f: " << sizeof(f) << endl;
	cout << "c: " <<  sizeof(c) << endl;
	cout << "10: " <<  sizeof(10) << endl;
	cout << "int: " <<  sizeof(int) << endl;
	cout << "double: " <<  sizeof(double) << endl;

	return 0;
}
